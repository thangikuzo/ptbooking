class Challenge {
  final String id;
  final String title;
  final String description;
  final String imageUrl;
  final int points;

  // Đây là Constructor - Cái phễu để đổ dữ liệu vào
  Challenge({
    required this.id,
    required this.title,
    required this.description,
    required this.imageUrl,
    required this.points,
  });
}